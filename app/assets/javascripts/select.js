$(function () {for (var i = 0, len = contact_array.length; i < len; ++i) {
    var contact_item = contact_array[i];
    $('#contact-selector').append("<option value=\"" + contact_item.Title + "\" data-address=\"" + contact_item.Address + "\" data-description=\"" + contact_item.Copy + "\" data-alternative-spellings=\"" + contact_item.Alternatives + "\">" + contact_item.Title + "</option>");
  };
});

(function($){
  $(function(){
    $('select#contact-selector').selectToAutocomplete();
    $('#contact-selector').change(function() {
      $('#contact-title').text($('#contact-selector option:selected').text());
      $('#contact-title').attr('href', $('#contact-selector option:selected').data('address')); 
      $('#contact-description').text($('#contact-selector option:selected').data('description')); 
      $('#contact-info').show('fast');
    });
    return false;
  });
})(jQuery);
